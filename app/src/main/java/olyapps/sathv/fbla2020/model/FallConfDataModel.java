public class FallConfDataModel {
    String lastname, firstname, gradyear, fallconf, fallpermi;

    public String getLastname() {
        return lastname;
    }

    public void setLastname(String lastname) {
        this.lastname = lastname;
    }

    public String getFirstname() {
        return firstname;
    }

    public void setFirstname(String firstname) {
        this.firstname = firstname;
    }

    public String getGradyear() {
        return gradyear;
    }

    public void setGradyear(String gradyear) {
        this.gradyear = gradyear;
    }

    public String getFallconf() {
        return fallconf;
    }

    public void setFallconf(String fallconf) {
        this.fallconf = fallconf;
    }

    public String getFallpermi() {
        return fallpermi;
    }

    public void setFallpermi(String fallpermi) {
        this.fallpermi = fallpermi;
    }
}
